values1 = ["Hello", "World", "!", "Good", "Morning", "!"]
print("values1 index:{0} count:{1}".format(values1.index("World"), values1.count("!")))

values2 = ["World", "Wide", ".", "!", "Morning", "!"]
print("values2 index:{0} count:{1}".format(values2.index("World"), values2.count("!")))