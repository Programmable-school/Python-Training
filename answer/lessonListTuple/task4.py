values1 = ["Hello", "World", "!", "Good", "Morning", "!"]
print("values1", values1)
print("values1 index: %d count: %d" % (values1.index("World"), values1.count("!")))

values2 = ["World", "Wide", ".", "!", "Morning", "!"]
print("values2", values2)
print("values2 index: %d count: %d" % (values2.index("World"), values2.count("!")))