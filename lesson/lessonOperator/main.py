# 計算演算子
value = 10
r1 = value + 3    # 足し算
r2 = value * 3    # 掛け算
r3 = value / 3    # 割り算
r4 = value // 3   # 除算
r5 = value % 3    # 余り
r6 = value ** 3   # べき乗
print(r1, r2, r3, r4, r5, r6) # 13 30 3.3333333333333335 3 1 1000


