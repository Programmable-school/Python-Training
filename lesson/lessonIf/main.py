# if文
def func1(value):
  if value == 1:
    print("value is 1")
  elif value == 3:
    print("value is 3")
  else:
    print("value is not 1")

def func2(score):
  if (80 <= score and score <= 100):
    print("Good")
  elif (60 <= score and score < 80):
    print("Normal")
  elif (20 <= score and score < 60):
    print("Bad")
  else:
    print("ahahaha..")

# 条件演算子
def func3(score):
  print("Good" if 100 >= score and score >= 80 else "ahahaha")

func1(1)  # value is 1
func1(2)  # value is not 1
func1(3)  # value is 3

func2(96) # Good
func2(72) # Normal
func2(27) # Bad
func2(0)  # ahahaha..

func3(100)  # Good
func3(79)   # ahahaha
