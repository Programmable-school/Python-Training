"""
数学関数
    https://docs.python.org/ja/3/library/math.html
"""
import math

# 乗数 
print(math.pow(2, 3))       # 8

# 絶対値
print(math.fabs(-20.2))    # 20.2

# 切り上げ
print(math.ceil(5.71))      # 6
print(math.ceil(5.21))      # 6

# 小数点以下を切り捨てて最大整数を返す
print(math.floor(5.71))     # 5
print(math.floor(-5.71))    # -6
